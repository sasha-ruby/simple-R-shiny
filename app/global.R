# Function to dynamically create plotly charts
plotmy <-
  function(pmData,
           pmxAxis,
           pmyAxis,
           pmName,
           pmType,
           pmMarker,
           pmTitle,
           pmTraces) {
    # configure chart
    plotme <- plot_ly(
      pmData %>% arrange(desc(pmyAxis)),
      x = pmxAxis,
      y = pmyAxis,
      name = pmName,
      type = pmType,
      marker = list(color = pmMarker),
      titlefont = list(size = 12)
    ) %>%
      layout(
        title = pmTitle,
        xaxis = axisFormat,
        yaxis = axisFormat,
        margin = marginFormat,
        legend = legendFormat
      ) %>%
      config(displayModeBar = F)

    # add traces dynamically
    for (pmTrace in pmTraces) {
      plotme <- add_trace(
        plotme,
        y = pmTrace[['y']],
        x = pmTrace[['x']],
        name = pmTrace[['name']],
        marker = list(color = pmTrace[['color']]) #,
        #evaluate = TRUE
      )
    }

    plotme
  }
